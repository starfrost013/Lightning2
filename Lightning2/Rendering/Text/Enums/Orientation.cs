
namespace LightningGL
{
    /// <summary>
    /// Orientation
    /// 
    /// Enumerates various types of text orientation
    /// </summary>
    public enum Orientation
    {
        /// <summary>
        /// Left to right text orientation.
        /// </summary>
        LeftToRight = 0,

        /// <summary>
        /// Right to left text orientation.
        /// </summary>
        RightToLeft = 1,

        /// <summary>
        /// Top to bottom text orientation.
        /// </summary>
        TopToBottom = 2,

        /// <summary>
        /// Bottom to top text orientation.
        /// </summary>
        BottomToTop = 3,
    }
}
