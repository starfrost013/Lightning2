namespace LightningGL
{
    /// <summary>
    /// FontSmoothingType (Freetype version)
    /// Enumerates the valid font smoothing types used for the Text API.
    /// </summary>
    public enum FontSmoothingType
    {
        /// <summary>
        /// Indicates that the text will be LCD blended and antialiased. The default
        /// </summary>
        Default = LCD,

        /// <summary>
        /// No smoothing done at all.
        /// </summary>
        Solid = 0,

        /// <summary>
        /// Indicates
        /// </summary>
        LCD = 3,
    }
}